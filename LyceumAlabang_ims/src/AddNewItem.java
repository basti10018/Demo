    private void saveBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBActionPerformed
        // TODO add your handling code here:
        LocalDateTime now = LocalDateTime.now();

        int ans = JOptionPane.showConfirmDialog(null, "Are you sure?", "WARNING",  JOptionPane.YES_NO_OPTION);
        if (ans == JOptionPane.YES_OPTION) {
            if (checkFields()) {

                try {

                    if (dt.addNewItem(particularTF.getText(), quantity, unitsList.getSelectedItem().toString(), datePurchased, barcodeTF.getText(),
                            unitPrice, unitCost, statusList.getSelectedItem().toString(), departmentList.getSelectedItem().toString())) {
                        String transaction = "added new item";
                        String name = StringUtils.capitalize(this.name) + " " + StringUtils.capitalize(this.lastname);
                        String data = StringUtils.capitalize(particularTF.getText());

                        dt.activityLogs(id, name, this.access, transaction, data, date.format(now), time.format(now), this.department);
                        JOptionPane.showMessageDialog(null, "Item successfully added");
                        dispose();

                    }

                } catch (Exception e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(null, "Error saving item ");
                }

            }

        }
    }//GEN-LAST:event_saveBActionPerformed

