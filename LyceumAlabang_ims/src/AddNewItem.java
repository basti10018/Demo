    boolean checkFields() {

        datePurchased = ((JTextField) dateChooser.getDateEditor().getUiComponent()).getText();
        try {
            quantity = Integer.parseInt(quantityTF.getText());

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Numeric value only on quanity field");
            return false;
        }

        try {
            unitPrice = Double.parseDouble(unitPriceTF.getText());

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Numeric value only on unit price field");
            return false;
        }

        try {
            unitCost = Double.parseDouble(costPriceTF.getText());

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Numeric value only on unit cost field");
            return false;
        }

        if (datePurchased.isEmpty() || particularTF.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Fill out all tab");
            return false;
        }
        return true;
    }

    void getUserId(String id) {
        this.id = id;
        dt.clearUserArray();
        dt.getUser(id);

        this.username = dt.user_username.get(0);
        this.name = dt.user_name.get(0);
        this.lastname = dt.user_lastname.get(0);
        this.access = dt.user_access.get(0);
        this.department = dt.user_department.get(0);

    }

    void getDept(String department) {
        if (department.equals("HRM")) {
            departmentList.setSelectedIndex(0);
            departmentList.setEnabled(false);
        } else if (department.equals("Chem lab")) {
            departmentList.setSelectedIndex(1);
            departmentList.setEnabled(false);
        } else if (department.equals("Supplies")) {
            departmentList.setSelectedIndex(2);
            departmentList.setEnabled(false);
        } else if (department.equals("Bookstore")) {
            departmentList.setSelectedIndex(3);
            departmentList.setEnabled(false);
        } else if (department.equals("Electronics lab")) {
            departmentList.setSelectedIndex(4);
            departmentList.setEnabled(false);
        }

    }

